# (old_value, new_value) — applied in order. Neither old string is a substring
# of the other, so REPLACE cannot double-apply.
_RENAMES = [
    ("ProgramAccountant", "Accountant"),
    ("CountryProgramLead", "Program Lead"),
]


def _apply(schema_editor, renames):
    """Run parameterized UPDATEs against each role column.

    Uses schema_editor so the SQL executes on the same connection as the
    migration. Raw SQL is required because many target tables belong to apps
    other than `accounts` and we deliberately do not import their models here
    (avoids coupling and stale-model risks during a data migration).
    """
    with schema_editor.connection.cursor() as cursor:
        for table, column, is_array in _ROLE_COLUMNS:
            for old, new in renames:
                if is_array:
                    # Postgres ArrayField: swap matching array elements.
                    sql = f'UPDATE "{table}" SET "{column}" = array_replace("{column}", %s, %s)'
                else:
                    sql = f'UPDATE "{table}" SET "{column}" = REPLACE("{column}", %s, %s)'
                cursor.execute(sql, [old, new])


def forward(apps, schema_editor):
    _apply(schema_editor, _RENAMES)


def reverse(apps, schema_editor):
    # Inverse mapping, reversed so the original order is restored.
    _apply(schema_editor, [(new, old) for old, new in reversed(_RENAMES)])


