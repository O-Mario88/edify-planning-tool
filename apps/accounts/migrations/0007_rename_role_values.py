# Data migration: rewrite stored role values to the new canonical names.
#
# EdifyRole values renamed in apps/core/rbac.py:
#   "ProgramAccountant"  -> "Accountant"
#   "CountryProgramLead" -> "Program Lead"
#
# These values are persisted across many role-bearing CharField columns (some
# with EdifyRole choices, most without) and across the user.roles Postgres
# ArrayField. This migration rewrites every existing row so it matches the new
# enum values.
#
# It is idempotent: running it twice is a no-op because REPLACE/array_replace
# only match the exact old strings, which no longer exist after the first run.
#
# Historical migration files are intentionally left untouched (they are frozen
# schema definitions). This migration layers the rename on top.
#
# NOTE: choices are not enforced at the DB level by Django, so the schema
# AlterField operations in 0006 only update Django's recorded state. The actual
# row data is rewritten here.
#
# The (table, column, is_array) list below was generated by introspecting the
# live model metadata (every field whose name contains "role" and is a
# CharField/ArrayField). `permission.roles` is a ForeignKey to Role, not a role
# string, and is intentionally excluded. `staff_setup_candidate.suggested_role`
# is free-text and not constrained; it's included because it may hold the old
# canonical value when inferred from a CCEO/PL assignment.

from django.db import migrations


