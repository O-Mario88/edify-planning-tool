// Shared page header for director / leadership surfaces (RVP country
// summary, leaderboard, weekly debrief reports, field intelligence).
//
// Now an async SERVER adapter around the canonical <PageHeader>: it
// resolves the signed-in user, computes the role-scoped FilterScope, and
// renders a live <HeaderFilterBar> (FY/Quarter/Region/District + Advanced
// drawer) instead of the old decorative pills. Title, subtitle, search,
// and breadcrumbs flow through as before. Bell + avatar + ⌘K come for
// free from PageHeader.

import { PageHeader } from "@/components/ui/PageHeader";
import { HeaderFilterBar } from "@/components/shell/HeaderFilterBar";
import { directorHeader } from "@/lib/director-mock";
import { getCurrentUser } from "@/lib/auth";
import { getFilterScope } from "@/lib/filters/scope-service";
import { liveDistrictNamesFor } from "@/lib/api/surfaces";

export async function ExecutiveHeader({
  title = directorHeader.title,
  subtitle = directorHeader.subtitle,
  breadcrumbTrailingLabel,
  breadcrumb: _legacyBreadcrumb,
}: {
  title?:    string;
  subtitle?: string;
  /** Final breadcrumb override — set when the trailing crumb should
   *  read as the entity name rather than the generic route label. */
  breadcrumbTrailingLabel?: string;
  /** @deprecated — breadcrumbs are now auto-generated by PageHeader
   *  from the URL via the route-titles map. Pass `breadcrumbTrailingLabel`
   *  instead. Kept on the type to avoid breaking existing callers. */
  breadcrumb?: readonly string[];
} = {}) {
  void _legacyBreadcrumb;
  const user = await getCurrentUser();
  const liveDistrictNames = await liveDistrictNamesFor(user);
  const scope = getFilterScope({ user, liveDistrictNames });

  return (
    <PageHeader
      title={title}
      subtitle={subtitle}
      filterBar={<HeaderFilterBar scope={scope} />}
      searchPlaceholder={directorHeader.searchPlaceholder}
      breadcrumbTrailingLabel={breadcrumbTrailingLabel}
    />
  );
}
